package nonPrimitiveDataType;

public class String1 
{
//String:-
	     //Non-primitive data type in java ( size is not fixed )
	     //String is sequence of character 
	     //Allows alpha-numeric values too.
	     //Immutable in nature -----> we cannot modify string 
	       
	//Syntax:-
	// a)String literal
	           //example:-   String S="VELOCITY";
	
	// b)String with a new keyword
	           //example:-   String S=new String ("Avinash");
	
	public static void main(String[] args)
	{
//String is sequence of character
		
	    String a="avinash";           //here "avinash" is example of literal String
		System.out.println(a);
		
		//Allows alpha-numeric values too.
		
		String b="avinash123";
		System.out.println(b);
		
		//Allows special character too.
		
		String c="avinash123@gmail.com";
		System.out.println(c);
		
		
//b)String with a new keyword
		
// here String act as class or we can say it is "default class" ,so we create object(x) with help of class (String).
		
		String x=new String ("Avinash");
		System.out.println(x);
		
		
		
		
	}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
